package comanda.entity;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "Monedas") // Esto debe coincidir con el nombre de la tabla tal cual en bd.
public class Moneda {

	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY) // para que la pk sea autoincremental y la estrategia de c√≥mo se
														// va a generar (en mysql).
	@Column(name = "MONEDA_ID")
	private Integer id;
	@Column(name = "MONEDA_NOMBRE")
	private String nombre;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	@Override
	public String toString() {
		return "Moneda [id=" + id + ", nombre=" + nombre + "]";
	}

}
