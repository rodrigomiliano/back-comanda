package comanda.entity;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "EstadisFactus") // Esto debe coincidir con el nombre de la tabla tal cual en bd.
public class EstadisFactu {

	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY) // para que la pk sea autoincremental y la estrategia de c√≥mo se
														// va a generar (en mysql).
	@Column(name = "EstadisFactus")
	private Integer id;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	@Override
	public String toString() {
		return "EstadisFactu [id=" + id + "]";
	}
	
	
}
